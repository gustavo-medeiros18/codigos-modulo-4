print("Usando range com um parâmetro:")
# Utilizando range() com um parâmetro, a sequência gerada irá de
# 0 até o valor anterior ao parâmetro. Nesse exemplo, irá de
# 0 a 9, de modo que o for terá 10 rodadas.
for i in range(10):
    print(i)

print("\nUsando range com dois parâmetros:")
# Utilizando range() com dois parâmetros, a sequência gerada irá
# do primeiro parâmetro ao valor anterior ao segundo parâmetro.
# Nesse exemplo, irá de 1 a 9, de modo que o for terá 9 rodadas.
for i in range(1, 10):
    print(i)